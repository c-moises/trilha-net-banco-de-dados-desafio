SELECT
	NOME,
	ANO
FROM FILMES

-- 2 
SELECT
	NOME, ANO
FROM
	FILMES
ORDER BY
	ANO ASC

-- 3
SELECT
	NOME, ANO, DURACAO
FROM 
	FILMES
WHERE
	NOME = 'DE VOLTA PARA O FUTURO'

-- 4
SELECT
	NOME, ANO
FROM
	FILMES
WHERE
	ANO = 1997

-- 5
SELECT
	NOME, ANO
FROM
	FILMES
WHERE
	ANO > 2000
ORDER BY
	ANO ASC

-- 6
SELECT
	NOME, DURACAO
FROM
	FILMES
WHERE
	DURACAO > 100 AND DURACAO < 150
ORDER BY
	DURACAO ASC

-- 7
SELECT
	ANO, COUNT(*) AS QUANTIDADEFILMES
FROM 
	FILMES
GROUP BY
	ANO
ORDER BY
	QUANTIDADEFILMES DESC

-- 8
SELECT
	PRIMEIRONOME, ULTIMONOME, GENERO
FROM
	ATORES
WHERE
	GENERO = 'M'

-- 9
SELECT
	PRIMEIRONOME, ULTIMONOME, GENERO
FROM
	ATORES
WHERE
	GENERO = 'F'
ORDER BY
	PRIMEIRONOME ASC

-- 10
SELECT
	F.NOME AS FILME, G.GENERO AS GENERO
FROM 
	FILMES F
JOIN 
	FILMESGENERO FG ON F.ID = FG.IDFILME
JOIN
	GENEROS G ON FG.IDGENERO = G.ID

-- 11
SELECT
	F.NOME AS FILME, G.GENERO AS GENERO
FROM
	FILMES F
JOIN
	FILMESGENERO FG ON F.ID = FG.IDFILME
JOIN
	GENEROS G ON FG.IDGENERO = G.ID
WHERE
	G.GENERO = 'MISTÉRIO'

-- 12
SELECT
	F.NOME AS FILME, A.PRIMEIRONOME, A.ULTIMONOME, EF.PAPEL
FROM
	FILMES F
JOIN
	ELENCOFILME EF ON F.ID = EF.IDFILME
JOIN
	ATORES A ON EF.IDATOR = A.ID
ORDER BY
	F.NOME, A.ULTIMONOME

